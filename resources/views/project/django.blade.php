       <h1>Développer une application Objet à l’aide du langage Python et du framework Django</h1>
        <p>Ce projet a consisté à créer un jeu à créer un site de gestion d'utilisateurs avec Django sous Python afin de découvrir ce Framework. </p>  
        <br>
        <p>Le projet consiste a laisser à l'utilisateur la capacité de choisir un rôle (pirate, citoyen, soldat), avec des caractéristiques (pouvoir, arme, nom). Une fois le personnage créer, il est possible de consulter les détails de celui-ci ou d'autres créés précédemment, de le modifier ou d'en rajouter.      
