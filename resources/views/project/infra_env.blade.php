       <h1>Installation d'une Infrastructure en environnement web - Bedrock</h1>
        
       <p>Le contexte est le suivant : Vous travaillez pour la société Bedrock qui connait une importante progression dans le sud-ouest de la France. Créée il y a 5 ans à Bordeaux, elle compte aujourd'hui 40 salariés et ses effectifs croissent de 20% chaque année. elle doit aujourd'hui déployer une nouvelle agence à Toulouse et employer des agents commerciaux à Biarritz, Limoges, Nantes et Pau. Les employés doivent accéder à l'application métier intranet (web privée) de l'entreprise ainsi qu'au site vitrine (web public) Vous êtes en charge de l'infrastructure premettant ces accès.</p>
       <br>
       <p>Sur le Siège de Bordeaux et l'agence de Toulouse, vous allez mettre en place un Firewall PfSense. Ce Firewall permettra d'isoler les postes des salariés de mettre à disposition des internautes le site web de l'entreprise sur l'agence de Toulouse (qui dispose d'une connexion Internet avec un bon débit et un faible délai de latence) Les 2 firewall Siège - Agence, seront connectés avec un VPN site à site les agents commerciaux pourront eux, se connecter à l'application métier via un VPN client-serveur, le serveur étant sur le siège de Bordeaux
        </p>
